    static func seedIfNeeded(context: ModelContext) {
        let itemDescriptor = FetchDescriptor<TrackItem>()
        let existingItems = (try? context.fetch(itemDescriptor)) ?? []
        if existingItems.isEmpty {
            seedTrackItems(context: context)
        }

        let quoteDescriptor = FetchDescriptor<Quote>()
        let existingQuotes = (try? context.fetch(quoteDescriptor)) ?? []
        if existingQuotes.isEmpty {
            seedQuotes(context: context)
        }

        let articleDescriptor = FetchDescriptor<Article>()
        let existingArticles = (try? context.fetch(articleDescriptor)) ?? []
        if existingArticles.isEmpty {
            seedArticles(context: context)
        }

        try? context.save()
    }

